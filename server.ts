class Ulid {
    private counter: number;

    constructor() {
        this.counter = 0;
    }

    new(): string {
        this.counter += 1;
        return `0001${Date.now().toString(16)}${this.counter.toString(16).padStart(10, '0')}`;
    }
}

interface CellObserver {
    updateCell(cellId: string, newValue: number, x: number, y: number): void;
}

class MockServer implements CellObserver {
    updateCell(cellId: string, newValue: number, x: number, y: number): void {
        console.log(`Cell at (${x}, ${y}) updated to ${newValue} (ID: ${cellId})`);
    }
}

class Cell {
    id: string;
    value: number;
    x: number; // X coordinate
    y: number; // Y coordinate
    observer?: CellObserver;

    constructor(idGenerator: Ulid, value: number, x: number, y: number, observer?: CellObserver) {
        this.id = idGenerator.new();
        this.value = value;
        this.x = x;
        this.y = y;
        this.observer = observer;
    }

    setValue(newValue: number): void {
        this.value = newValue;
        if (this.observer) {
            this.observer.updateCell(this.id, newValue, this.x, this.y);
        }
    }
}

class ResonanceGrid {
    grid: Cell[][];
    observer?: CellObserver;

    constructor(matrix: number[][], observer?: CellObserver) {
        const ulid = new Ulid();
        // create a 2d array of cells from the matrix and ulid generator here 
        this.grid = matrix.map((row, x) => row.map((value, y) => new Cell(ulid, value, x, y, observer)));
        this.observer = observer;
    }

    async randomUpdate(): Promise<void> {
        // Choose between 1-5 cells to update randomly
        const updatesCount = Math.floor(Math.random() * 5) + 1;
        for (let i = 0; i < updatesCount; i++) {
            const x = Math.floor(Math.random() * this.grid.length);
            const y = Math.floor(Math.random() * this.grid[0].length);
            const newValue = Math.floor(Math.random() * 10);
            this.grid[x][y].setValue(newValue);
        }
    }

    startRandomUpdates(): void {
        const interval = Math.floor(Math.random() * 9000) + 1000; // 1-10 seconds
        setTimeout(async () => {
            await this.randomUpdate();
            this.startRandomUpdates();
        }, interval);
    }
}

